    private void lbl_close_frameMouseEntered(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_close_frameMouseEntered
        // TODO add your handling code here:
        if (Tema.modo_tema.equals("Default")) {
            lbl_close_frame.setBackground(Tema.COLOR_DARK);
            lbl_close_frame.setCursor(Cursor.getPredefinedCursor(Cursor.HAND_CURSOR));
        } else if (Tema.modo_tema.equals("Dark")) {
            lbl_close_frame.setBackground(Tema.COLOR_DEFAULT);
            lbl_close_frame.setCursor(Cursor.getPredefinedCursor(Cursor.HAND_CURSOR));
        }
    }//GEN-LAST:event_lbl_close_frameMouseEntered

    private void lbl_close_frameMouseExited(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lbl_close_frameMouseExited
        // TODO add your handling code here:
        if (Tema.modo_tema.equals("Default")) {
            lbl_close_frame.setBackground(Tema.COLOR_DEFAULT);
            lbl_close_frame.setCursor(Cursor.getDefaultCursor());
        } else if (Tema.modo_tema.equals("Dark")) {
            lbl_close_frame.setBackground(Tema.COLOR_DARK);
            lbl_close_frame.setCursor(Cursor.getDefaultCursor());
        }
    }//GEN-LAST:event_lbl_close_frameMouseExited

